function CellFactory_GetTags(matid)
    local results = {}
    for _, entry in ipairs(CELL_FACTORY_RAW) do
        local mid, mtype, mname, muiname, mtags = unpack(entry)
        if mid == matid then
            local tags = {}
            for tag in mtags:gmatch("%[[^,]+%]") do
                table.insert(tags, tag)
            end
            return tags
        end
    end
    print_error(("CellFactory_GetTags: Invalid material %s"):format(matid))
    return nil
end


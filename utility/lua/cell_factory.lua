function CellFactory_GetType(matname)
    for _, entry in ipairs(CELL_FACTORY_RAW) do
        local mid, mtype, mname, muiname, mtags = unpack(entry)
        if mname == matname then
            return mid
        end
    end
    print_error(("CellFactory_GetType: Invalid material %s"):format(matname))
    return nil
end

